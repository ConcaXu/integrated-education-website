$(document).ready(function() {
    let currentLang = 'zh-CN';

    // Mobile Menu Toggle
    $('.mobile-menu-btn').click(function() {
        $('.navbar').toggleClass('active');
        $(this).find('i').toggleClass('fa-bars fa-times');
    });

    // Close mobile menu when clicking a link
    $('.navbar a').click(function() {
        if ($(window).width() <= 768) {
            $('.navbar').removeClass('active');
            $('.mobile-menu-btn i').removeClass('fa-times').addClass('fa-bars');
        }
    });

    // Reset nav on resize
    $(window).resize(function() {
        if ($(window).width() > 768) {
            $('.navbar').removeClass('active');
            $('.mobile-menu-btn i').removeClass('fa-times').addClass('fa-bars');
        }
    });

    // Header Scroll Effect
    $(window).scroll(function() {
        if ($(this).scrollTop() > 50) {
            $('.header').addClass('scrolled');
        } else {
            $('.header').removeClass('scrolled');
        }
    });

    // Smooth Scrolling
    $('a[href^="#"]').on('click', function(event) {
        if (this.hash !== "") {
            event.preventDefault();
            var hash = this.hash;
            
            // Adjust offset based on header height
            var headerHeight = $('.header').outerHeight();
            
            $('html, body').animate({
                scrollTop: $(hash).offset().top - headerHeight
            }, 800);
        }
    });

    // FAQ Accordion
    $('.faq-question').click(function() {
        const item = $(this).parent('.faq-item');
        const isActive = item.hasClass('active');
        
        // Close all other items
        $('.faq-item').removeClass('active');
        
        // Toggle current item
        if (!isActive) {
            item.addClass('active');
        }
    });

    // Language Switcher
    $('#lang-switch-btn').click(function(e) {
        e.preventDefault();
        if (currentLang === 'zh-CN') {
            currentLang = 'en';
            $(this).find('.current-lang-text').text('EN English');
        } else {
            currentLang = 'zh-CN';
            $(this).find('.current-lang-text').text('CN 简体中文');
        }
        
        updateLanguage(currentLang);
    });

    function updateLanguage(lang) {
        // Update text content
        $('.lang-text').each(function() {
            const key = $(this).data('key');
            if (translations[lang][key]) {
                $(this).text(translations[lang][key]);
            }
        });

        // Update attributes (like placeholder)
        $('.lang-attr').each(function() {
            const key = $(this).data('key');
            const attr = $(this).data('attr');
            if (translations[lang][key]) {
                $(this).attr(attr, translations[lang][key]);
            }
        });
        
        // Update html lang attribute
        $('html').attr('lang', lang);
    }
    
    // Initialize with default language
    updateLanguage(currentLang); 
});
